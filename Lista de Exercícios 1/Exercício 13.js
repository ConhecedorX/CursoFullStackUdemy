function calendario (dia) {
  switch (dia){
    case 1:
      console.log("Hoje é domingo, bora aproveitar e sair!")
      break
    case 2:
      console.log('Hoje é segunda-feira, dia útil temos que trabalhar!')
      break
    case 3:
      console.log('Hoje é terça-feira, dia útil temos que trabalhar!')
      break
    case 4:
      console.log('Hoje é quarta-feira, dia útil temos que trabalhar!')
      break
    case 5:
      console.log('Hoje é quinta-feira, dia útil temos que trabalhar!')
      break
    case 6:
      console.log('Hoje é sexta-feira, dia útil temos que trabalhar!')
      break
    case 7:
      console.log("Hoje é sábado, bora aproveitar e sair!")
      break
    default:
      console.log("Dia inválido...")
  }
}

